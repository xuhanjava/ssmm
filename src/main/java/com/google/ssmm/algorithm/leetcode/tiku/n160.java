public class n160 {
    public class Solution {
        public ListNode getIntersectionNode(ListNode headA, ListNode headB) {
            int a,b;
            while(headA != null){
                a = headA.val;
            }
            while(headB != null){
                b = headB.val;
            }
            return null;
        }
    }
}

    static boolean find(int[] nums,int begin,int end,int target){
        if (begin > end){
            return false;
        }
        int middle = (end + begin) / 2;
        if(nums[middle] == target){
            return true;
        }else if(nums[begin] == nums[middle]){
            return find(nums,begin +1,end,target);
        }else if (nums[begin] > nums[middle]){
            if(binarySearch(nums, middle + 1, end, target)) {
                return true;
            }
            return find(nums,begin,middle-1,target);
        }else{
            if(binarySearch(nums, begin, middle - 1, target)) {
                return true;
            }
            return find(nums,middle +1,end,target);
        }
    }

    static boolean binarySearch(int[] nums,int begin,int end,int target){
        if(begin > end){
            return false;
        }
        int middle = (end + begin) / 2;
        if(nums[middle] == target){
            return true;
        }
        else if(nums[middle] > target){
           return binarySearch(nums,begin,end - 1,target);
        }else{
           return binarySearch(nums,middle +1,end ,target);
        }
    }


